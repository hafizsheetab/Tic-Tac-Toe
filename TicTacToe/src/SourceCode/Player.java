package SourceCode;

public class Player {
    String playerName = "";
    String playerValue = "";

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public String getPlayerValue() {
        return playerValue;
    }

    public void setPlayerValue(String playerValue) {
        this.playerValue = playerValue;
    }
}
